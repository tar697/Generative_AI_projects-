    image = Image.open(uploaded_file).convert("RGB")
    st.image(image, caption="Uploaded Image", use_column_width=True)

    if st.button("âœ¨ Generate Caption"):
        with st.spinner("Generating caption..."):
            inputs = processor(images=image, return_tensors="pt")
            out = model.generate(**inputs)
            caption = processor.decode(out[0], skip_special_tokens=True)

            if caption_style == "Poetic":
                styled_caption = f"âœ¨ In hues of dreams: {caption}"
            elif caption_style == "Funny":
                styled_caption = f"ðŸ˜‚ When your brush has a sense of humor: {caption}"
            elif caption_style == "Mystical":
                styled_caption = f"ðŸ”® A vision from another realm: {caption}"
